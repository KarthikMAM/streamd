#!/usr/bin/env node
/**
 * Regenerate `packages/spec/src/skip.ts` by running every CommonMark
 * and GFM spec fixture through `@streamd/parser` + `@streamd/html` and
 * comparing against the reference HTML.
 *
 * Without `--annotate`, every cluster lands in the output with a
 * default `classification: "under-investigation"` annotation. With
 * `--annotate`, existing cluster-level annotations from the current
 * `skip.ts` are preserved and attached to the regenerated output;
 * brand-new clusters still default to `under-investigation`.
 *
 * Usage:
 *   node packages/spec/scripts/collect-failures.mjs              # dry run
 *   node packages/spec/scripts/collect-failures.mjs --write      # write skip.ts
 *   node packages/spec/scripts/collect-failures.mjs --annotate --write
 *
 * @module spec.scripts.collect-failures
 */

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

/** Directory layout. */
const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const SPEC_PKG = dirname(SCRIPT_DIR);
const FIXTURES_DIR = join(SPEC_PKG, "fixtures");
const SKIP_PATH = join(SPEC_PKG, "src", "skip.ts");

/** CLI flags. */
const ARGS = new Set(process.argv.slice(2));
const SHOULD_WRITE = ARGS.has("--write");
const SHOULD_ANNOTATE = ARGS.has("--annotate");

/**
 * Extract the cluster key (spec-section slug) from a fixture basename
 * like "0256--list-items" â†’ "list-items".
 */
function clusterKeyOf(fixtureName) {
  const idx = fixtureName.indexOf("--");
  if (idx < 0) return "unknown";
  return fixtureName.slice(idx + 2);
}

/**
 * Discover `.md` / `.html` fixture pairs in a directory. Returns a
 * sorted array of basenames without extension.
 */
function discoverFixtures(dir) {
  return readdirSync(dir)
    .filter((f) => f.endsWith(".md"))
    .sort()
    .map((f) => basename(f, ".md"));
}

/**
 * Collapse whitespace + strip comments for a tolerant HTML compare.
 * Kept in sync with `spec.test.ts`'s `normalizeHtml` behaviour.
 */
async function normalizeHtml(html, minify) {
  if (!html.trim()) return "";
  try {
    return await minify(html, {
      collapseWhitespace: true,
      removeComments: true,
      sortAttributes: true,
    });
  } catch {
    return html.replace(/\s+/g, " ").trim();
  }
}

/**
 * Run one fixture through parser + renderer. Returns true when the
 * produced HTML matches the reference.
 */
async function fixturePasses(directory, fixtureName, gfm, deps) {
  const markdown = readFileSync(join(directory, `${fixtureName}.md`), "utf-8");
  const expected = readFileSync(join(directory, `${fixtureName}.html`), "utf-8");
  const tokens = deps.parse(markdown, null, { gfm }).tokens;
  const actual = deps.renderHtml(tokens);
  const normActual = await normalizeHtml(actual, deps.minify);
  const normExpected = await normalizeHtml(expected, deps.minify);
  return normActual === normExpected;
}

/**
 * Run a single suite (commonmark | gfm) and return the list of
 * fixtures whose output diverges from the reference.
 */
async function collectSuiteFailures(suiteDir, gfm, deps) {
  const failures = [];
  const fixtures = discoverFixtures(suiteDir);
  for (const name of fixtures) {
    const passed = await fixturePasses(suiteDir, name, gfm, deps);
    if (!passed) failures.push(name);
  }
  return { failures, total: fixtures.length };
}

/**
 * Group flat fixture names by cluster, preserving original sort order
 * within each cluster.
 */
function groupByCluster(fixtureNames) {
  const groups = new Map();
  for (const name of fixtureNames) {
    const key = clusterKeyOf(name);
    if (!groups.has(key)) groups.set(key, []);
    groups.get(key).push(name);
  }
  return groups;
}

/**
 * Default annotation for brand-new clusters that the current skip.ts
 * has no entry for.
 */
function defaultAnnotation() {
  return {
    classification: "under-investigation",
    notes: "Auto-generated by collect-failures.mjs â€” classification pending manual triage.",
  };
}

/**
 * Import the existing `SKIP_METADATA` from the current skip.ts, if
 * any. Returns a Map<suite, Map<clusterKey, annotation>> so we can
 * merge when --annotate is passed.
 */
async function loadExistingAnnotations() {
  try {
    const mod = await import(SKIP_PATH);
    const meta = mod.SKIP_METADATA;
    if (!meta) return new Map();
    const out = new Map();
    for (const suiteKey of Object.keys(meta)) {
      const clusters = new Map();
      for (const [clusterKey, cluster] of Object.entries(meta[suiteKey].clusters)) {
        clusters.set(clusterKey, cluster.annotation);
      }
      out.set(suiteKey, clusters);
    }
    return out;
  } catch {
    return new Map();
  }
}

/**
 * Render one annotation as its TS object literal. Keeps the field
 * order stable: classification â†’ notes â†’ docLinkInParserDesign â†’
 * trackingUrl.
 */
function renderAnnotation(annotation, indent) {
  const pad = " ".repeat(indent);
  const inner = " ".repeat(indent + 2);
  const parts = [
    `${inner}classification: ${JSON.stringify(annotation.classification)},`,
    `${inner}notes: ${JSON.stringify(annotation.notes)},`,
  ];
  if (annotation.docLinkInParserDesign) {
    parts.push(
      `${inner}docLinkInParserDesign: ${JSON.stringify(annotation.docLinkInParserDesign)},`,
    );
  }
  if (annotation.trackingUrl) {
    parts.push(`${inner}trackingUrl: ${JSON.stringify(annotation.trackingUrl)},`);
  }
  return `{\n${parts.join("\n")}\n${pad}}`;
}

/**
 * Render one cluster block (annotation + examples).
 */
function renderCluster(clusterKey, cluster, indent) {
  const pad = " ".repeat(indent);
  const inner = " ".repeat(indent + 2);
  const annot = renderAnnotation(cluster.annotation, indent + 2);
  const exampleLines = cluster.examples.map((e) => `${inner}  ${JSON.stringify(e)},`).join("\n");
  const key = /^[A-Za-z_$][\w$]*$/.test(clusterKey) ? clusterKey : JSON.stringify(clusterKey);
  return [
    `${pad}${key}: {`,
    `${inner}annotation: ${annot},`,
    `${inner}examples: [`,
    exampleLines,
    `${inner}],`,
    `${pad}},`,
  ].join("\n");
}

/**
 * Render a full suite block (`commonmark: { clusters: { â€¦ } }`).
 */
function renderSuite(suiteKey, clusters, indent) {
  const pad = " ".repeat(indent);
  const inner = " ".repeat(indent + 2);
  const body = [];
  for (const [clusterKey, cluster] of clusters) {
    body.push(renderCluster(clusterKey, cluster, indent + 4));
  }
  return [
    `${pad}${suiteKey}: {`,
    `${inner}clusters: {`,
    body.join("\n"),
    `${inner}},`,
    `${pad}},`,
  ].join("\n");
}

/**
 * Build the full skip.ts contents from the collected failures.
 */
function renderSkipFile(collected, existing, stats) {
  const header = buildHeader(stats);
  const suites = [];
  for (const [suiteKey, failures] of collected) {
    const groups = groupByCluster(failures);
    const clusters = new Map();
    const existingClusters = existing.get(suiteKey) ?? new Map();
    for (const [clusterKey, examples] of groups) {
      const annotation = existingClusters.get(clusterKey) ?? defaultAnnotation();
      clusters.set(clusterKey, { annotation, examples });
    }
    suites.push(renderSuite(suiteKey, clusters, 2));
  }
  const meta = [
    "/**",
    " * Full annotated skip metadata. Edit here â€” do not hand-edit `SKIP`",
    " * below.",
    " */",
    "export const SKIP_METADATA: Readonly<Record<SuiteKey, SkipSuite>> = {",
    suites.join("\n"),
    "};",
  ].join("\n");
  return [header, SKIP_TYPES, meta, SKIP_RUNTIME, ""].join("\n\n");
}

/**
 * Render the module-level JSDoc header with the latest pass rates.
 */
function buildHeader(stats) {
  const fmt = (s) => {
    const pct = ((s.pass / s.total) * 100).toFixed(1);
    return `${s.pass} / ${s.total} = ${pct} %`;
  };
  return [
    "/**",
    " * Known spec divergences â€” tests skipped against reference output.",
    " *",
    " * Source of truth: `SKIP_METADATA`. Each cluster (a spec section, e.g.",
    ' * "tabs", "block-quotes") carries:',
    " *",
    " * - `classification`: one of `documented-limitation` | `fixable` |",
    " *   `known-bug` | `under-investigation`.",
    " * - `notes`: one-sentence explanation of the divergence.",
    " * - `docLinkInParserDesign`: optional pointer into",
    " *   `.kiro/steering/parser-design.md` Â§11 Known Limitations.",
    " * - `trackingUrl`: optional ticket URL for `known-bug` clusters.",
    " *",
    " * Consumers that only need the runtime skip set (e.g. `spec.test.ts`)",
    " * read the flat `SKIP: Record<suite, Set<fixture>>` view, which is",
    " * derived from `SKIP_METADATA` at module load.",
    " *",
    " * Rebuild after any parser / renderer change:",
    " *",
    " *     node packages/spec/scripts/collect-failures.mjs --annotate --write",
    " *",
    " * Pass rate snapshot (CommonMark 0.31.2 / GFM 0.29):",
    ` *   CommonMark: ${fmt(stats.commonmark)}`,
    ` *   GFM:        ${fmt(stats.gfm)}`,
    " *",
    " * @module spec.skip",
    " */",
  ].join("\n");
}

/** Static `type`/`interface` block â€” must match the hand-written version. */
const SKIP_TYPES = [
  "/**",
  " * Classification buckets for a skipped cluster.",
  " *",
  " * - `documented-limitation` â€” divergence is an accepted trade-off and",
  " *   has a matching entry in `parser-design.md` Â§11.",
  " * - `fixable` â€” divergence is a bug we intend to fix. Not in Â§11 yet.",
  " * - `known-bug` â€” divergence is a bug that contradicts a design",
  " *   statement elsewhere in `parser-design.md` and has a tracking",
  " *   ticket.",
  " * - `under-investigation` â€” root cause not yet diagnosed; needs a",
  " *   pass through `collect-failures.mjs` and manual triage.",
  " */",
  "export type SkipClassification =",
  '  | "documented-limitation"',
  '  | "fixable"',
  '  | "known-bug"',
  '  | "under-investigation";',
  "",
  "/**",
  " * Metadata attached to a single skip cluster.",
  " */",
  "export interface SkipClusterAnnotation {",
  "  readonly classification: SkipClassification;",
  "  readonly notes: string;",
  "  readonly docLinkInParserDesign?: string;",
  "  readonly trackingUrl?: string;",
  "}",
  "",
  "/**",
  " * A skip cluster: annotation + the fixture names in the cluster.",
  " * Fixture names match the `.md`/`.html` basename in `fixtures/<suite>`.",
  " */",
  "export interface SkipCluster {",
  "  readonly annotation: SkipClusterAnnotation;",
  "  readonly examples: ReadonlyArray<string>;",
  "}",
  "",
  "/**",
  " * Structured skip data for one suite, keyed by spec section slug",
  " * (everything after `NNNN--` in the fixture basename).",
  " */",
  "export interface SkipSuite {",
  "  readonly clusters: Readonly<Record<string, SkipCluster>>;",
  "}",
  "",
  "/**",
  " * Known suite keys. Must match directory names under",
  " * `packages/spec/fixtures/`.",
  " */",
  'export type SuiteKey = "commonmark" | "gfm";',
].join("\n");

/** Static runtime helper block â€” kept at the bottom of skip.ts. */
const SKIP_RUNTIME = [
  "/**",
  " * Flatten a `SkipSuite` into the raw set of fixture names used by",
  " * `spec.test.ts` for `.skip` dispatch. Stable across calls.",
  " */",
  "function flattenSuite(suite: SkipSuite): Set<string> {",
  "  const out = new Set<string>();",
  "  for (const cluster of Object.values(suite.clusters)) {",
  "    for (const example of cluster.examples) out.add(example);",
  "  }",
  "  return out;",
  "}",
  "",
  "/**",
  " * Runtime skip view â€” identical shape to the previous flat export, so",
  " * `spec.test.ts` can keep using `SKIP[skipKey].has(name)` without",
  " * changes.",
  " */",
  "export const SKIP: Record<SuiteKey, Set<string>> = {",
  "  commonmark: flattenSuite(SKIP_METADATA.commonmark),",
  "  gfm: flattenSuite(SKIP_METADATA.gfm),",
  "};",
].join("\n");

/**
 * Top-level driver.
 */
async function main() {
  const parserMod = await import("@streamd/parser");
  const htmlMod = await import("@streamd/html");
  const minifierMod = await import("html-minifier-terser");
  const deps = {
    parse: parserMod.parse,
    renderHtml: htmlMod.renderHtml,
    minify: minifierMod.minify,
  };

  const cmDir = join(FIXTURES_DIR, "commonmark");
  const gfmDir = join(FIXTURES_DIR, "gfm");

  const cm = await collectSuiteFailures(cmDir, false, deps);
  const gfm = await collectSuiteFailures(gfmDir, true, deps);

  const stats = {
    commonmark: { pass: cm.total - cm.failures.length, total: cm.total },
    gfm: { pass: gfm.total - gfm.failures.length, total: gfm.total },
  };

  console.log(`CommonMark: ${stats.commonmark.pass} / ${stats.commonmark.total} pass`);
  console.log(`GFM:        ${stats.gfm.pass} / ${stats.gfm.total} pass`);
  console.log(`Failures:   ${cm.failures.length} (CM) + ${gfm.failures.length} (GFM)`);

  const existing = SHOULD_ANNOTATE ? await loadExistingAnnotations() : new Map();
  const collected = new Map([
    ["commonmark", cm.failures],
    ["gfm", gfm.failures],
  ]);
  const rendered = renderSkipFile(collected, existing, stats);

  if (SHOULD_WRITE) {
    writeFileSync(SKIP_PATH, rendered, "utf-8");
    console.log(`Wrote ${SKIP_PATH}`);
  } else {
    console.log(`\nDry run â€” pass --write to overwrite skip.ts.`);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
